#include <iostream>
#include "54_Point.h"
using namespace std;

// 헤더파일에서 선언한 함수들 정의
bool Point::InitMembers(int xpos, int ypos)
{
	if (xpos < 0 || ypos < 0)
	{
		cout << "벗어난 범위의 값 전달" << endl;
		return false;
	}

	x = xpos;
	y = ypos;
	return true;
}

int Point::GetX() const  // 엑세스함수
{
	return x;
}

int Point::GetY() const  // 엑세스함수
{
	return y;
}

bool Point::SetX(int xpos)
{
	if (0 > xpos  || xpos > 100)
	{
		cout << "벗어난 범위의 값 전달" << endl;
		return false;
	}
	x = xpos;
	return true;
}
bool Point::SetY(int ypos)
{
	if (0 > ypos || ypos > 100)
	{
		cout << "벗어난 범위의 값 전달" << endl;
		return false;
	}
	y = ypos;
	return true;
}